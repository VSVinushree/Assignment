package robosoft;
import java.util.*;
public class Swap {
	public static void main(String args[]) {
		Scanner sc=new Scanner(System.in);
		int a=24;
		int b=44;
		System.out.println("before swap");
		System.out.println("a="+a);
		System.out.println("b="+b);
		int temp=a;
		a=b;
		b=temp;
		System.out.println("after swap");
		System.out.println("a="+a);
		System.out.println("b="+b);
		
				
		
	}

}
